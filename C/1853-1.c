// 탑다운 방식
#include <stdio.h>


int n;
 
int f(int k) { // 탑다운 방식 
	if(k<=1) return 1; // k가 1보다 작거나 같으면 1 리턴 
	return f(k-1)+k; // f(2)+3
	// f(1)+2
	// f(1) = 1
	// f(1)+2 = f(2) = 3
	// f(2) + 3 = 3+3 = 6
}

/*
	n=5
	f(5) -> f(4)+5 
	f(4) -> f(3)+4
	f(3) -> f(2)+3
	f(2) -> f(1)+2
	f(1) -> return 1
	f(2) = f(1)+2 = 1+2 = 3
	f(3) = f(2)+3 = 3+3 = 6
	f(4) = f(3)+4 = 6+4 = 10
	f(5) = f(4)+5 = 10+5 = 15 
*/

int main() {
	scanf("%d", &n); // n=3
	printf("%d", f(n)); // f(3)
	return 0;
}